class Solution
{

public:
    int xmod11(string x)
    {
       // code here
       int cur=0; int rem=0;
	    for(int i=0 ;i<x.length(); i++)
	    {
	        cur = rem*10 + x[i]-'0';
	        rem = cur%11;
	    }
	    return rem;
    }
};


